def disasm(code):
    i, out = 0, []
    while i < len(code):
        op = code[i]
        if 0x60 <= op <= 0x7f:
            n = op - 0x5f
            out.append((i, "PUSH" + str(n), "0x" + code[i + 1:i + 1 + n].hex()))
            i += 1 + n
        else:
            out.append((i, OPCODES.get(op, "UNK_" + format(op, "02x")), ""))
            i += 1
    return out


